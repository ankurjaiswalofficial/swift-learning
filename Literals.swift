
import Foundation

// Integer literal
let intLiteral = 42
print("Integer Literal: \(intLiteral)")

// Floating-point literal
let floatLiteral = 3.14
print("Float Literal: \(floatLiteral)")

// String literal
let stringLiteral = "Hello, Swift!"
print("String Literal: \(stringLiteral)")

// Boolean literal
let boolLiteral = true
print("Boolean Literal: \(boolLiteral)")

// Array literal
let arrayLiteral = [1, 2, 3, 4, 5]
print("Array Literal: \(arrayLiteral)")

// Dictionary literal
let dictLiteral = ["key1": "value1", "key2": "value2"]
print("Dictionary Literal: \(dictLiteral)")
