
import Foundation

// Integer
var integerVar: Int = 42
print("Integer: \(integerVar)")

// Floating-point
var floatVar: Float = 3.14
print("Float: \(floatVar)")

// Double
var doubleVar: Double = 3.141592653589793
print("Double: \(doubleVar)")

// Boolean
var boolVar: Bool = true
print("Boolean: \(boolVar)")

// String
var stringVar: String = "Hello, Swift!"
print("String: \(stringVar)")

// Character
var charVar: Character = "A"
print("Character: \(charVar)")
